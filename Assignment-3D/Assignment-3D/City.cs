        }

        public int GetBuildingHeight
        {
            get { return this.buildingstyles.Max(); }
        }

        public Vector3 Position
        {
            get { return this.citybuilder.Position; }
        }

        public Vector3 Center
        {
            get { return new Vector3((this.Position.X + this.GetWidth) / 2, (this.Position.Y + this.GetBuildingHeight) / 2, (this.Position.Z + this.GetHeight) / 2); }
        }

        public BoundingBox CityBounds
